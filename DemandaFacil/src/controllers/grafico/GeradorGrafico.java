    private CategoryDataset createDataset() { 
    DefaultCategoryDataset dataset = new DefaultCategoryDataset(); 
        dataset.addValue(10,"Picanha","Outubro");
        dataset.addValue(8.0,"Carvão","Outubro");
        dataset.addValue(7.0,"Palheiro","Outubro");
        dataset.addValue(9.0,"Cerveja","Outubro");
    return dataset; 
}

    public void criaGrafico() {
    CategoryDataset cds = createDataset();
    String titulo = "Total de Produtos";
    String eixoy = "Quantidade";
    String txt_legenda = "Mês";
    boolean legenda = true;
    boolean tooltips = false ;
    boolean urls = false;
    JFreeChart graf = ChartFactory.createBarChart3D(titulo,txt_legenda, eixoy, cds, PlotOrientation.VERTICAL, legenda,tooltips, urls);
    ChartPanel myChartPanel = new ChartPanel(graf, true);
    myChartPanel.setSize(jPanel1.getWidth(),jPanel1.getHeight());
    myChartPanel.setVisible(true); 
    jPanel1.removeAll();
    jPanel1.add(myChartPanel); 
    jPanel1.revalidate();
    jPanel1.repaint(); 
}
    

    
